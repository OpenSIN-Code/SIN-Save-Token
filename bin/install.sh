#!/usr/bin/env bash
# ============================================================================
# SIN-Save-Token — one-shot installer for the 4-layer token-savings standard.
#
# Idempotent. Run it as many times as you like; it converges to a compliant
# state and NEVER duplicates hooks/plugins. Designed so NO agent ever has to
# be reminded to save tokens — every runtime self-uses the standard.
#
# Usage:   ./bin/install.sh            # install/repair everything
#          ./bin/install.sh --check    # verify only (no changes)
#          ./bin/install.sh --heal     # quiet self-heal (used by SessionStart hook)
#
# Layers (see docs/BEST-PRACTICES.md for the evidence base):
#   L1 shell    — rtk transparent command compression (PreToolUse hook / plugin)
#   L2 tools    — thin MCP surface; NO aggressive tool-search deferral (breaks /compact caching)
#   L3 memory   — shared claude-mem worker + single DB (no double-spend)
#   L4 output   — terse output contract in every runtime's instruction file
# ============================================================================
set -euo pipefail

MODE="${1:-install}"
say()  { [ "$MODE" = "--heal" ] || echo "$@"; }
warn() { echo "⚠️  $@" >&2; }

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

# ── L1: rtk on PATH ─────────────────────────────────────────────────────────
if ! command -v rtk >/dev/null 2>&1; then
  warn "rtk not found on PATH. Install it first:"
  warn "    cargo install rtk    # or: brew install rtk (RTK - Rust Token Killer)"
  warn "    Verify with:  rtk --version && rtk gain"
  [ "$MODE" = "--check" ] && exit 1
else
  say "✅ L1 rtk present ($(rtk --version 2>/dev/null | head -1))"
fi

# ── Claude Code: rtk PreToolUse hook + lean settings ────────────────────────
CC_SETTINGS="$HOME/.claude/settings.json"
if [ -f "$CC_SETTINGS" ] && command -v rtk >/dev/null 2>&1; then
  if [ "$MODE" != "--check" ]; then
    # rtk init is idempotent — it will not double-add its hook.
    rtk init --claude >/dev/null 2>&1 || rtk init >/dev/null 2>&1 || true
  fi
  say "✅ Claude Code rtk hook ensured"
fi

# ── opencode: rtk plugin (auto-loaded from plugins/) ────────────────────────
OC_PLUGINS="$HOME/.config/opencode/plugins"
if [ -d "$HOME/.config/opencode" ]; then
  mkdir -p "$OC_PLUGINS"
  if [ ! -f "$OC_PLUGINS/rtk.ts" ] && command -v rtk >/dev/null 2>&1; then
    [ "$MODE" != "--check" ] && (cd "$HOME/.config/opencode" && rtk init --opencode >/dev/null 2>&1 || true)
  fi
  [ -f "$OC_PLUGINS/rtk.ts" ] && say "✅ opencode rtk plugin present" || warn "opencode rtk.ts missing — run: cd ~/.config/opencode && rtk init"
fi

# ── Codex: RTK.md reference in both possible homes ──────────────────────────
for CODEX_HOME in "$HOME/.codex" "$HOME/Library/Application Support/orca/codex-runtime-home/home"; do
  [ -d "$CODEX_HOME" ] || continue
  if [ ! -f "$CODEX_HOME/RTK.md" ] && command -v rtk >/dev/null 2>&1; then
    [ "$MODE" != "--check" ] && (cd "$CODEX_HOME" && rtk init --codex >/dev/null 2>&1 || rtk init >/dev/null 2>&1 || true)
  fi
  [ -f "$CODEX_HOME/RTK.md" ] && say "✅ Codex RTK.md present ($CODEX_HOME)"
done

# ── SessionStart self-heal hook for Claude Code ─────────────────────────────
# This is what makes the standard SELF-MAINTAINING: every new session quietly
# re-runs the installer in --heal mode, so a hook/plugin that ever goes missing
# is silently restored. No human, no agent reminder needed.
HOOK_DIR="$HOME/.claude/hooks"
HEAL_HOOK="$HOOK_DIR/sin-save-token-heal.sh"
if [ "$MODE" != "--check" ] && [ -d "$HOME/.claude" ]; then
  mkdir -p "$HOOK_DIR"
  cat > "$HEAL_HOOK" <<HEAL
#!/usr/bin/env bash
# Auto-generated by SIN-Save-Token. Self-heals the token-savings standard on
# every session start. Safe, quiet, idempotent.
"$REPO_DIR/bin/install.sh" --heal >/dev/null 2>&1 || true
exit 0
HEAL
  chmod +x "$HEAL_HOOK"
  say "✅ Self-heal SessionStart hook written: $HEAL_HOOK"
  say "   → Register it once in ~/.claude/settings.json under hooks.SessionStart"
  say "     (the installer does NOT edit your hook array automatically — see README)"
fi

# ── Verify ──────────────────────────────────────────────────────────────────
if [ -x "$REPO_DIR/bin/verify-tokens" ]; then
  say ""
  "$REPO_DIR/bin/verify-tokens" || { [ "$MODE" = "--check" ] && exit 1 || true; }
fi

say ""
say "🎉 SIN-Save-Token install/heal complete."
