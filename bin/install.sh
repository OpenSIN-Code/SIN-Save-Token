#!/usr/bin/env bash
# ============================================================================
# SIN-Save-Token — one-shot installer for the 4-layer token-savings standard.
#
# Idempotent. Run it as many times as you like; it converges to a compliant
# state and NEVER duplicates hooks/plugins. Designed so NO agent ever has to
# be reminded to save tokens — every runtime self-uses the standard.
#
# Usage:   ./bin/install.sh            # install/repair everything
#          ./bin/install.sh --check    # verify only (no changes)
#          ./bin/install.sh --heal     # quiet self-heal (used by SessionStart hook)
#
# Layers (see docs/BEST-PRACTICES.md for the evidence base):
#   L1 shell    — rtk transparent command compression (PreToolUse hook / plugin)
#   L2 tools    — thin MCP surface; NO aggressive tool-search deferral (breaks /compact caching)
#   L3 memory   — shared claude-mem worker + single DB (no double-spend)
#   L4 output   — terse output contract in every runtime's instruction file
# ============================================================================
set -euo pipefail

MODE="${1:-install}"
say()  { [ "$MODE" = "--heal" ] || echo "$@"; }
warn() { echo "⚠️  $@" >&2; }

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

# ── L1: rtk on PATH ─────────────────────────────────────────────────────────
if ! command -v rtk >/dev/null 2>&1; then
  warn "rtk not found on PATH. Install it first:"
  warn "    cargo install rtk    # or: brew install rtk (RTK - Rust Token Killer)"
  warn "    Verify with:  rtk --version && rtk gain"
  [ "$MODE" = "--check" ] && exit 1
else
  say "✅ L1 rtk present ($(rtk --version 2>/dev/null | head -1))"
fi

# ── Claude Code: official rtk PreToolUse hook + lean settings ───────────────
# Prefer the official `rtk hook claude` registration so `rtk gain` stays quiet.
# SST also ships hooks/rtk-auto-rewrite.js as a compatible dual path (issue #4).
CC_SETTINGS="$HOME/.claude/settings.json"
if [ -f "$CC_SETTINGS" ] && command -v rtk >/dev/null 2>&1; then
  if [ "$MODE" != "--check" ]; then
    # -g --auto-patch: register official PreToolUse hook without prompting.
    rtk init -g --auto-patch >/dev/null 2>&1 \
      || rtk init -g --hook-only --auto-patch >/dev/null 2>&1 \
      || rtk init >/dev/null 2>&1 \
      || true
  fi
  say "✅ Claude Code rtk hook ensured (rtk init -g --auto-patch)"
fi

# ── opencode: rtk plugin (auto-loaded from plugins/) ────────────────────────
OC_PLUGINS="$HOME/.config/opencode/plugins"
if [ -d "$HOME/.config/opencode" ]; then
  mkdir -p "$OC_PLUGINS"
  if [ ! -f "$OC_PLUGINS/rtk.ts" ] && command -v rtk >/dev/null 2>&1; then
    [ "$MODE" != "--check" ] && (cd "$HOME/.config/opencode" && rtk init --opencode >/dev/null 2>&1 || true)
  fi
  [ -f "$OC_PLUGINS/rtk.ts" ] && say "✅ opencode rtk plugin present" || warn "opencode rtk.ts missing — run: cd ~/.config/opencode && rtk init"
fi

# ── Codex: RTK.md reference in both possible homes ──────────────────────────
for CODEX_HOME in "$HOME/.codex" "$HOME/Library/Application Support/orca/codex-runtime-home/home"; do
  [ -d "$CODEX_HOME" ] || continue
  if [ ! -f "$CODEX_HOME/RTK.md" ] && command -v rtk >/dev/null 2>&1; then
    [ "$MODE" != "--check" ] && (cd "$CODEX_HOME" && rtk init --codex >/dev/null 2>&1 || rtk init >/dev/null 2>&1 || true)
  fi
  [ -f "$CODEX_HOME/RTK.md" ] && say "✅ Codex RTK.md present ($CODEX_HOME)"
done

# ── SessionStart self-heal hook for Claude Code ─────────────────────────────
# This is what makes the standard SELF-MAINTAINING: every new session quietly
# re-runs the installer in --heal mode, so a hook/plugin that ever goes missing
# is silently restored. No human, no agent reminder needed.
HOOK_DIR="$HOME/.claude/hooks"
HEAL_HOOK="$HOOK_DIR/sin-save-token-heal.sh"
if [ "$MODE" != "--check" ] && [ -d "$HOME/.claude" ]; then
  mkdir -p "$HOOK_DIR"
  cat > "$HEAL_HOOK" <<HEAL
#!/usr/bin/env bash
# Auto-generated by SIN-Save-Token. Self-heals the token-savings standard on
# every session start. Safe, quiet, idempotent.
# 1. re-install silently (restore any hook/plugin that drifted away)
"$REPO_DIR/bin/install.sh" --heal >/dev/null 2>&1 || true
# 2. surface compliance drift LOUD (green = silent; any 🚨 REGRESSION printed).
#    Verbose layers stay suppressed; only failures reach the session. --quiet
#    keeps the green COMPLIANT line off the transcript so a healthy host is silent.
if "$REPO_DIR/bin/verify-tokens" --quiet >/tmp/sst-verify.out 2>&1; then
  :
else
  grep -E '🚨|REGRESSION|^\[' /tmp/sst-verify.out 2>/dev/null >&2 || true
fi
rm -f /tmp/sst-verify.out
exit 0
HEAL
  chmod +x "$HEAL_HOOK"
  say "✅ Self-heal SessionStart hook written: $HEAL_HOOK"
  say "   → Register it once in ~/.claude/settings.json under hooks.SessionStart"
  say "     (the installer does NOT edit your hook array automatically — see README)"
fi

# ── agent-grep: structure-augmented search on PATH + PreToolUse nudge ───────
# agent-grep tags each hit with its enclosing symbol and self-truncates, so the
# agent rarely needs a follow-up file read. We (1) put it on PATH and (2) register
# two idempotent PreToolUse hooks: agent-grep-nudge (points broad Grep-tool calls
# at agent-grep) and cache-cold-warn (flags a >5min prompt-cache gap).
if [ "$MODE" != "--check" ]; then
  BIN_DEST="$HOME/.local/bin"
  mkdir -p "$BIN_DEST"
  ln -sfn "$REPO_DIR/bin/agent-grep" "$BIN_DEST/agent-grep"
  say "✅ agent-grep linked -> $BIN_DEST/agent-grep"

  # memory-scope (jcode idea ②) + session-digest (idea ③): on PATH, NO hooks.
  # memory-scope's own --audit gate says selective-retrieval wiring is negative
  # ROI until the memory corpus is large; session-digest is an on-demand handoff.
  ln -sfn "$REPO_DIR/bin/memory-scope" "$BIN_DEST/memory-scope"
  ln -sfn "$REPO_DIR/bin/session-digest" "$BIN_DEST/session-digest"
  # dream (mimo /dream, ported): mines durable lessons from a session into shared
  # memory. Reuses session-digest's adapters, so it reads Claude + opencode.
  ln -sfn "$REPO_DIR/bin/dream" "$BIN_DEST/dream"
  say "✅ memory-scope + session-digest + dream linked -> $BIN_DEST/"

  if [ -f "$CC_SETTINGS" ] && command -v python3 >/dev/null 2>&1; then
    AGN="$REPO_DIR/hooks/agent-grep-nudge.js"
    CCW="$REPO_DIR/hooks/cache-cold-warn.js"
    python3 - "$CC_SETTINGS" "$AGN" "$CCW" <<'PY' && say "✅ PreToolUse hooks (agent-grep-nudge, cache-cold-warn) registered"
import json, sys, os
settings, agn, ccw = sys.argv[1], sys.argv[2], sys.argv[3]
try:
    d = json.load(open(settings))
except Exception:
    sys.exit(0)  # never break the installer on a malformed settings file
pt = d.setdefault("hooks", {}).setdefault("PreToolUse", [])

def registered(script):
    return any(script in h.get("command", "")
               for grp in pt for h in grp.get("hooks", []))

added = False
# Grep tool → agent-grep nudge
if not registered("agent-grep-nudge"):
    pt.append({"matcher": "Grep",
               "hooks": [{"type": "command", "command": f'node "{agn}"'}]})
    added = True
# any tool → cache-cold warn (matcher "*" = every tool call)
if not registered("cache-cold-warn"):
    pt.append({"matcher": "*",
               "hooks": [{"type": "command", "command": f'node "{ccw}"'}]})
    added = True

if added:
    tmp = settings + ".tmp"
    json.dump(d, open(tmp, "w"), indent=2)
    os.replace(tmp, settings)
PY
  fi
fi

# ── Verify ──────────────────────────────────────────────────────────────────
if [ -x "$REPO_DIR/bin/verify-tokens" ]; then
  say ""
  "$REPO_DIR/bin/verify-tokens" || { [ "$MODE" = "--check" ] && exit 1 || true; }
fi

say ""
say "🎉 SIN-Save-Token install/heal complete."
